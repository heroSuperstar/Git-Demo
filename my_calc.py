def add_numbers(num_1, num_2):
    result = num_1 + num_2

    return result

def sub_numbers(num_1, num_2):
    result = num_1 - num_2

    return result

def multiply_numbers(num_1, num_2):

    result = num_1 * num_2

    return result

def modulus_numbers(num_1, num_2):
    result = num_1 % num_2

    return result

def divide_numbers(num_1, num_2):
    result = num_2 / num_1

    return result

def greater_numbers(num_1, num_2):

    if(num_1 > num_2):
        result = num_1
    else:
        result = num_2

    return result

def multi_pi(num_1):
    
    result = num_1 * 3.1415926535 #multiply a number by 10 digit Pi

    return result